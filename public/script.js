function showHide() {
    let x = document.getElementById("userForm");
    
    if (x.style.display == "block") {
      x.style.display = "none";
    } else {
      x.style.display = "block";
    }
  }
  
  
const loader = document.querySelector('.loader');
window.addEventListener('load', () => {
  loader.style.display = 'none';
});
  
  
  
  
  
  